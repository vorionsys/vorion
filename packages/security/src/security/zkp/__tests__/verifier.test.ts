  describe('isExpired with clockSkewTolerance boundaries', () => {
    it('isExpired returns false when exactly at boundary', () => {
      // isExpired: Date.now() > proof.expiresAt.getTime() + this.config.clockSkewTolerance
      // At boundary: now === expiresAt + tolerance → > returns false
      const now = Date.now();
      const proof = createMockProof({
        expiresAt: new Date(now - 5000), // expired 5s ago, tolerance is 5s
      });

      // now > (now - 5000) + 5000 → now > now → false
      expect(verifier.isExpired(proof)).toBe(false);
    });

    it('isExpired returns true when 1ms beyond boundary', () => {
      const now = Date.now();
      const proof = createMockProof({
        expiresAt: new Date(now - 5001), // expired 5.001s ago, tolerance is 5s
      });

      // now > (now - 5001) + 5000 → now > now - 1 → true
      expect(verifier.isExpired(proof)).toBe(true);
    });
  });

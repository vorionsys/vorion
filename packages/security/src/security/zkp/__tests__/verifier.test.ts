/**
 * Generate a real proof using the prover so it passes cryptographic verification.
 */
async function createValidProof(circuit = 'age_verification'): Promise<ZKProof> {
  const prover = new ZKProverService({ includeMetadata: true });
  return prover.generateAgeProof(new Date('1990-01-01'), 18);
}

/**
 * Build a mock proof with controllable fields.
 * The 96-byte proof passes structural checks but may not pass full crypto
 * unless generated through the prover.
 */
function createMockProof(overrides: Partial<ZKProof> = {}): ZKProof {
  return {
    proof: new Uint8Array(96),
    publicInputs: [Date.now().toString(), '18'],
    circuit: 'age_verification',
    timestamp: new Date(),
    expiresAt: new Date(Date.now() + 3600000),
    proofId: '12345678-1234-4234-8234-123456789abc',
    version: '1.0.0',
    ...overrides,
  };
}

/**
 * Serialize a ZKProof into the format accepted by verifySerializedProof.
 */
function serializeProof(proof: ZKProof): SerializedZKProof {
  // Convert Uint8Array to base64 via binary string
  const binaryString = String.fromCharCode(...proof.proof);
  const base64 = btoa(binaryString);

  return {
    proof: base64,
    publicInputs: proof.publicInputs,
    circuit: proof.circuit,
    timestamp: proof.timestamp.toISOString(),
    expiresAt: proof.expiresAt?.toISOString(),
    metadata: proof.metadata,
    version: proof.version,
    proofId: proof.proofId,
  };
}

